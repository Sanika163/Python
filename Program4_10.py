def main():
    print("Range 1 with step 1 starts from 1 to 10:")
    for i in range(1, 10):
        print(i)

    print("Range 2 with step 2 starts from 1 to 10:")
    for j in range(1,10,2):
        print(j)

if (__name__ == "__main__"):
    main()